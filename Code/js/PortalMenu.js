//OpeniFrame
function OpeniFrame() {
  if ((document.getElementById("iFramePortal").style.visibility = "hidden")) {
    document.getElementById("iFramePortal").style.visibility = "visible";
    document.getElementById("iFramePortal").style.visibility = "visible";
  }
}

